package Framework.HelperClasses;

public class Move {
    private int xPos;
    private int yPos;

    public Move(int xPos, int yPos) {
        this.xPos = xPos;
        this.yPos = yPos;
    }

    public int getX() {
        return xPos;
    }

    public int getY() {
        return yPos;
    }

}
